    private void submitCustomization() {

        String selectedVeggies = (String) veggiesComboBox.getSelectedItem();
        String selectedMeatItems = (String) meatItemsComboBox.getSelectedItem();
        String selectedSauces = (String) saucesComboBox.getSelectedItem();

        Ingredient veggies = ingredientFactory.getIngredient(selectedVeggies);
        Ingredient meatItems = ingredientFactory.getIngredient(selectedMeatItems);
        Ingredient sauces = ingredientFactory.getIngredient(selectedSauces);

        // Example: Customize the ingredients
        interpreterContext.interpret(veggies, (String) customCommandsComboBox.getSelectedItem());
        interpreterContext.interpret(meatItems, (String) customCommandsComboBox.getSelectedItem());
        interpreterContext.interpret(sauces, (String) customCommandsComboBox.getSelectedItem());

        // Print the customization details
        System.out.println("\nCustomization Details:");
        System.out.println("Selected Food Item: " + selectedFoodItem);
        System.out.println("Selected Toppings: ");
        System.out.println("  Veggies - " + veggies.getName());
        System.out.println("  Meat Items - " + meatItems.getName());
        System.out.println("  Sauces - " + sauces.getName());
        System.out.println("Selected Custom Command: " + customCommandsComboBox.getSelectedItem());

        ProcessPanel processPanel = new ProcessPanel();
        processPanel.setVisible(true);
        this.dispose();

    }

