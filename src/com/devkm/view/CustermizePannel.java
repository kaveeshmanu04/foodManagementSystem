   private void initializeToppingPrices() {
        toppingPrices = new HashMap<>();
        toppingPrices.put("Veggie", 100);
        toppingPrices.put("Meat Items", 300);
        toppingPrices.put("Sauces", 200);
    }

    public int calculateTotalPrice() {
        int totalPrice = 0;

        String selectedVeggie = (String) veggiesComboBox.getSelectedItem();
        String selectedMeatItem = (String) meatItemsComboBox.getSelectedItem();
        String selectedSauce = (String) saucesComboBox.getSelectedItem();

        totalPrice += getToppingPrice(selectedVeggie) * quantity;
        totalPrice += getToppingPrice(selectedMeatItem) * quantity;
        totalPrice += getToppingPrice(selectedSauce) * quantity;

        return totalPrice;
    }

    private int getToppingPrice(String topping) {
        return toppingPrices.getOrDefault(topping, 0);
    }
